#Continue loops

x = 0
while x < 6:
    x += 1
    if x ==4:
        continue
    print(x)

print("\n")

#Breaking While Loop Condition

a = 1
while a < 14:
    print(a)
    if a==4:
        print("it's equal to 4, time to stop!")
        break
    a+=1



