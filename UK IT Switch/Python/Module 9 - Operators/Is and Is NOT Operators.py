print("Membership Operators - IN")

numbers = [1,2,3,4]
print(2 in numbers)

colours = ['blue', 'red', 'yellow']
print('green' in colours)


print("Memebership Operators - NOT IN")

numbers = [1,2,3,4]
print(2 not in numbers)

colours = ['blue', 'red', 'yellow']
print('green' not in colours)

